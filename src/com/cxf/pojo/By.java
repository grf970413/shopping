package com.cxf.pojo;

public class By {
	private String byProName;
	private Integer id;
	private Integer mainProId;
	private String imgAddress;
	public String getByProName() {
		return byProName;
	}
	public void setByProName(String byProName) {
		this.byProName = byProName;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getMainProId() {
		return mainProId;
	}
	public void setMainProId(Integer mainProId) {
		this.mainProId = mainProId;
	}
	public String getImgAddress() {
		return imgAddress;
	}
	public void setImgAddress(String imgAddress) {
		this.imgAddress = imgAddress;
	}
}
